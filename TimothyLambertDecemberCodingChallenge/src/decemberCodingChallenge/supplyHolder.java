package decemberCodingChallenge;

public class supplyHolder
{

	private int xCoord;
	private int yCoord;
	
	public supplyHolder(int xCoord, int yCoord)
	{
		this.xCoord = xCoord;
		this.yCoord = yCoord;
	}
	
	public void setXCoord(int xCoord)
	{
		this.xCoord = xCoord;
	}
	
	public int getXCoord()
	{
		return xCoord;
	}
	
	public void setYCoord(int yCoord)
	{
		this.yCoord = yCoord;
	}
	
	public int getYCoord()
	{
		return yCoord;
	}
	
}
